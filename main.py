#Define the function addCheck() with if and else statements
def addCheck(x, y, z):
    if x + y == z:
        return True

    else:
        return False

#Print the function with 2, 2, and 4
print (addCheck(2, 2, 4))

#Print the function with 10, 3, and 13
print (addCheck(10, 3, 13))

#Print the function with 10, 3, and 30
print (addCheck(10, 3, 30))